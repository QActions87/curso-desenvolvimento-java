
public class ArrayNotasEstudos {

    public static void main(String[] args) {
        int[] idades = {18, 20, 30};

        System.out.println(idades[0]);
        System.out.println(idades[1]);
        System.out.println("==============");

        for (int idade : idades) {
              System.out.println(idade);
        }
    }
}

